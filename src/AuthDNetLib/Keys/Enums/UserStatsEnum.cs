namespace AuthDNetLib.Keys.Enums;

/// <summary>
/// Enumera os possíveis status de um usuário em no sistema de autenticação.
/// Fornece opções para determinar se um usuário está ativo ou inativo no sistema.
/// </summary>
public enum UserStatsEnum
{
    /// <summary>
    /// Representa um usuário ativo no sistema.
    /// </summary>
    ACTIVE = 1,

    /// <summary>
    /// Representa um usuário inativo no sistema.
    /// </summary>
    INACTIVE = 0
}